package chapter4;

public class StaticLesson {
    public static void main(String[] args) {
        Hamster h1 = new Hamster("Hamtaro", 1, "White, Orange");
        Hamster h2 = new Hamster("Micky", 60, "Black");
        System.out.println(h1.name);
        System.out.println(h2.name);
        System.out.println(h1.numberOfTth);
        System.out.println(h2.numberOfTth);
        h1.numberOfTth = 66;
        h2.numberOfTth = 166;
        Hamster.numberOfTth=1000;
        
        System.out.println(h1.numberOfTth);
        System.out.println(h2.numberOfTth);
        h1 = null;
        h2=null;
        System.out.println(h1.numberOfTth);
        System.out.println(h2.numberOfTth);
    }
}
